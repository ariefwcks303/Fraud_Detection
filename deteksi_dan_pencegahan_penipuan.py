  tn = eval_table.loc["Actual not Fraud", "Predicted not Fraud"]
  fp = eval_table.loc["Actual not Fraud", "Predicted Fraud"]

  n_pred_neg = tn + fp
  n_neg = tn

  FPR = fp / (n_pred_neg + 1e-6)
  return FPR

